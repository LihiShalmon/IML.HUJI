    #
    # diff_squared = (y_pred - y_true) ** 2
    # return np.mean(diff_squared)


def misclassification_error(y_true: np.ndarray, y_pred: np.ndarray, normalize: bool = True) -> float:
    """
    Calculate misclassification loss

    Parameters
    ----------
    y_true: ndarray of shape (n_samples, )
        True response values
    y_pred: ndarray of shape (n_samples, )
        Predicted response values
    normalize: bool, default = True
        Normalize by number of samples or not

    Returns
    -------
    Misclassification of given predictions
    """
    # check if values are equal in the i'th coordinate
    are_equal = np.array(y_pred != y_true)

    # return normalized / unNormalized error rate
    if normalize:
        return np.count_nonzero(are_equal)/are_equal.size
    else:
        return np.count_nonzero(are_equal)

def accuracy(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    """
    Calculate accuracy of given predictions

    Parameters
    ----------
    y_true: ndarray of shape (n_samples, )
        True response values
    y_pred: ndarray of shape (n_samples, )
        Predicted response values

    Returns
    -------
    Accuracy of given predictions
    """
    error_num = misclassification_error(y_true,y_pred)
    obeservation_num = y_true.length
    true_values = obeservation_num - error_num

    return true_values/obeservation_num


